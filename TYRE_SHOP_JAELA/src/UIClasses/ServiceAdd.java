    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        JTextField[] jt = {comserviceid, comservicename, comservicecategory, tfservicecost, tfservicepricegen};
        boolean result = es.checkEMPTY(jt);
        if(!result){
            try {
                ResultSet rs = DB.search("SELECT * FROM service WHERE service_id = '"+comserviceid.getText()+"'");
                if(rs.next()){
                    try {
                        DB.Execute("DELETE FROM service WHERE service_id = '"+comserviceid.getText()+"'");
                        DB.Execute("INSERT INTO service VALUES('"+comserviceid.getText()+"', '"+comservicename.getText()+"', '"+comservicecategory.getText()+"', '"+tfservicecost.getText()+"', '"+tfservicepricegen.getText()+"')");
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                } else{
                    try {
                        DB.Execute("INSERT INTO service VALUES('"+comserviceid.getText()+"', '"+comservicename.getText()+"', '"+comservicecategory.getText()+"', '"+tfservicecost.getText()+"', '"+tfservicepricegen.getText()+"')");
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
                es.clearTFS(jt);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

