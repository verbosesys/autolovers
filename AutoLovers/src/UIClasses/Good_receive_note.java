    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        JTextField[] jt = {tforderno, tfitemid, comitemname, tfqty, tfcost, tftotval, comstatus};
        boolean result = es.checkEMPTY(jt);
        if (result == false) {
            try {
                ResultSet rs = DB.search("SELECT * FROM grn WHERE orderno = '" + tforderno.getText() + "'");
                if (rs.next()) {
                    new ConfirmMessage("Modification", "Do you want to change data?", false) {
                        @Override
                        void ifYes() {
                            try {
                                DB.Execute("DELETE FROM grn WHERE orderno = '" + tforderno.getText() + "'");
                                DB.Execute("INSERT INTO grn VALUES('" + tforderno.getText() + "', '" + tfitemid.getText() + "', '" + comitemname.getText() + "', '" + tfqty.getText() + "', '" + tfcost.getText() + "', '" + tftotval.getText() + "', '" + comstatus.getText() + "')");
                                es.clearTFS(jt);
                                showData();
                            } catch (Exception ex) {
                                ex.printStackTrace();
                            }
                        }

                        @Override
                        void ifNo() {
                            return;
                        }
                    }.setVisible(true);
                } else {
                    DB.Execute("INSERT INTO grn VALUES('" + tforderno.getText() + "', '" + tfitemid.getText() + "', '" + comitemname.getText() + "', '" + tfqty.getText() + "', '" + tfcost.getText() + "', '" + tftotval.getText() + "', '" + comstatus.getText() + "')");
                    es.clearTFS(jt);
                    showData();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSendActionPerformed

